        <p>
        <h2>Advance Java Programming</h2>
        <h5>Introduction</h5>
        <p>: Introduction to basic java concepts, JDK, JRE, JVM, wrapper classes, inner and
            nested classes
            Unit 2
            Arrays and Strings: working with arrays and strings, String, String Buffer and String Builder
            classes, access specifies, inheritance.</p>
        <h5>Collection Framework:</h5>
        <p>Array List class, List Iterator interface, Linked list class, Tree Set
            class, Priority Queue class
            Unit 4
            More on Collection Framework: Comparable and Comparator, Properties class, Lambda
            expressions</p>
        <h5>Multithreading:</h5>
        <p>implementing multithreading, life cycle of a thread, thread communication
            Unit 6 More on Multithreading: suspending, resuming, deadlock and stopping threads
            Unit 7 Synchronization: thread synchronization, handling exceptions during multithreading.
                </p>

                
            <h5>Swings</h5>
            <p>: JButton class, JRadioButton class, JTextArea class, JComboBox class, JTable class.
                Unit 9 More on Swings: JColorChooser class, JProgressBar class, JSlider class
                Unit 10 Layouts: layout manager, Border Layout, Grid Layout, Flow Layout, Box Layout, Card
                Layout
            </p>

            <h5>Managing data using JDBC:</h5>
            <p>: introduction to JDBC, Connectivity with database, CRUD
                operations, Connection interface
                Unit 12 More on JDBC: Statement interface, Result Set interface, Prepared Statement, Result Set Meta
                Data, and Database Metadata
            </p>

            <h5>Network Programming</h5>
            <p>
                  Java network terminology, socket classes, server socket classes
                  Unit 14 More on Network Programming: URL class, URL connection class, Datagram Socket class,
                  Java socket programming
            </p>

            <p>
                <h2>Course Outcomes :</h2> 
        At the end of this course student will: <br>
        CO1: learn the structure and model of the Java programming language<br />
        CO2: understand the accessibility of fields and methods of an object through String and String Builder classes <br />
        CO3: develop applications in Java programming language to solve problems <br />
        CO4: evaluate user requirements for software functionality and assess its implementation in java <br />
        CO5: implement Lambda functions. <br/>
        CO6: demonstrate object serialization with file handling and exception handling to overcome run-time errors <br />
        
             </p>

    </p>

