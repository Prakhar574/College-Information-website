    <p>
        <h2>Big Data</h2>
        <h5>Introduction to Big data</h5>
        <p>Big Data and its importance, The V's of Big Data, Challenges and
            Applications of Big Data, Tools used in Big Data Scenario.
            Unit- 2
            Foundations for Big Data: Distributed file system, scalable computing over internet,
            programming models for big data</p>
        <h5>Data Models</h5>
        <p>Data model vs. data format, data stream, understanding data lakes, exploring
            streaming sensor data</p>
        <h5>NOSQL data management</h5>
        <p>Introduction to NoSQL, aggregate data models, aggregates keyvalue and document data models relationships, graph databases, schema less databases,
            materialized views, distribution models, sharding, version, Map reduce partitioning and
            combining, composing map-reduce calculations.
            </p> 
            
            <h5>Introduction to Hadoop</h5>
            <p>Understand what Hadoop is, learning about other open-source
                software related to Hadoop, understand how Big Data solutions can work on the Cloud,
                Hadoop - Big Data Overview, Hadoop - Big Data Solutions.
                Unit- 6
                Hadoop Administration: Hadoop - Environment Setup, Hadoop - HDFS Overview, Starting
                HDFS, Hadoop - Command Reference
                </p>

                
            <h5>Hadoop Architecture</h5>
            <p>
                Hadoop – Map Reduce, Hadoop – Streaming, Hadoop
                – Multi Node Cluster, Creating User Account, Configuring Key Based Login, Installing
                Hadoop and Configuring Hadoop on Master Server.
                Unit- 9
                Hadoop Node Commands: Configuring Master Node, Configuring Slave Node, Format
                Name Node on Hadoop Master, Starting Hadoop Services, Adding a New Data Node in the
                Hadoop Cluster, Adding User and SSH Access.
            </p>

            <h5>Map Reduce </h5>
            <p>Map Reduce workflows – unit tests with MR Unit – test data
                and local tests, anatomy of Map Reduce job run, classic Map-reduce, YARN failures in classic
                Map-reduce and YARN job scheduling, shuffle and sort, task execution, Map Reduce types,
                input formats, output formats
                
            </p>

            <p>
                <h2>Course Outcomes:</h2>
                CO1: analyze the need and importance of fundamental concepts and principles of Big Data <br />
                CO2: apply internal functioning of different modules of Big Data and Hadoop <br />
                CO3: evaluate the big data ecosystem and appreciate its key components <br />
            </p>

    </p>

