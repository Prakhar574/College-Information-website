    <p>
        <h2>Data Structures and Algorithms</h2>
        <h5>Introduction</h5>
        <p>.need of data structures and algorithms, time and space complexity of
            algorithms, asymptotic notations, average and worst case analysis,</h5>
        <p>operations on arrays and linked lists</p>
        <h5>Queues and Stacks</h5>
        <p> implementation of queues, priority queue, applications of queues,
            implementation of stacks, applications of stacks: quick sort, parenthesis checker,
            arithmetic expression conversion and evaluation, tower of Hanoi problem, role of stack in
             recursion,
            </p> 
            
            <h5>Search tree and tree data structure</h5>
            <p>AVL trees: balancing operations, b-trees: properties and operations,
                Unit- 7
                Tree data structure 2: red-black trees. splay trees: properties and operations, 2-3 trees:
                properties and operations
                Unit- 8
                Heaps: introduction to heaps, min heap, max heap, operations on heap, applications of heap:
                priority queue implementation
                Unit- 9 More on heaps: heap sort, binomial heaps, Fibonacci heaps
                </p>
                
            <h5>Graphs</h5>
            <p>
                type of graphs, adjacency matrix and linked adjacency chains, connected components
                and spanning trees
                
            </p>

            <h5>Hashing Techniques</h5>
            <p>Plinear list representation, hash table representation, hash functions
                Unit- 13 collision resolution: separate chaining, open addressing-linear probing, quadratic probing
                Unit- 14 More on hashing: double hashing, rehashing
                
                
            </p>

            <br />
   

            <p>
      <h2>Course Outcomes:</h2>
      CO1: perceive advanced data structures and perform operations on them <br />
      CO2: understand abstract data types and algorithmic complexity <br />
      CO3: apply suitable data structure for solving problems <br />
      CO4: implement hashing and collision resolution techniques <br />
      CO5: evaluate the performance of various algorithms <br />
      
             </p>

    </p>

