    <p>
        <h2>Web Technologies</h2>
        <h5>Introduction</h5>
        <p>Know about the building block ofweb pages that is the HTML</p>
        <h5>Cascading Style Sheet</h5>
        <p> concepts of selectors, formatting tags with CSS, responsive layout designing
        using CSS flexbox
        Unit- 3
        Introduction to Bootstrap: introduction to bootstrap, associating bootstrap with mobile web
        interfaces
        </p>
        <h5>Using the Bootstrap framework</h5>
        <p>starter template, bootstrap theme, bootstrap-grids, bootstrapjumbotron, bootstrap-narrow jumbotron
            Unit- 5 Navbars in action: bootstrap-navbar, bootstrap-static top navbar, bootstrap-fixed navbar
            
            
            </p> 
            
            <h5>Introduction to ReactJS:</h5>
            <p> Reactjs architecture, Reactjs and web development
                Unit- 8
                Pure React concepts: setting up webpage using react and react DOM, constructing elements
                with data, concept of DOM rendering, working with factories in react
            </p>

                
            <h5>Using React with JSX</h5>
            <p>
                : defining react elements using JSX, concept of trans piling and babel,
working with recipes and webpack
Unit- 10 State management and component tree in ReactJS: validating properties with react,
managing data using state in react, using component tree to manage state
            </p>

            <h5>Working with React router and server</h5>
            <p>web page management by incorporating react
                router, data driven web applications and router parameters, react based server rendering,
                react based server communication
                Unit- 12
                Components in detail: stateful vs stateless components, creating class-based components,
                more abou
                
            </p>

            <br />
   <br/>
   <br />

            <p>
                <h2>Course Outcomes:</h2>
                CO1: understand the website layout creation using HTML language. <br />
                CO2: apply the website planning, management and maintenance techniques <br />
                CO3: apply dynamic website creation using JavaScript and Query <br />
                CO4: illustrate logic implementation on a web page <br />
                CO5: understand how to manage versatile data on a web page <br />
                
             </p>

    </p>

