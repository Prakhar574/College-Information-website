    <p>
        <h2>Discrete Mathematics </h2>
        <h5>Introduction to software engineering:</h5>
        <p>Introduction, conjunction, disjunction & negation, propositions and truth table, Tautologies and
            contradictions, equivalence of formulas, duality law.
            Unit- 2
            Predicates, the statement function, variables and quantifiers, predicate formulas. Methods of
            proof (Inference Theory).</p>
        <h5>Partial Ordered Sets</h5>
        <p>External elements of POSET, HASSE Diagrams of POSETS, Well-Ordered
            Sets, Lattices, Bounded Lattices, Distributive Lattices,
            Unit- 4
            Introduction to Boolean algebra, Basic Definitions, Duality, Basic Theorems, Boolean Algebras
            as Lattices</p>
        <h5>Permutations and Combinations</h5>
        <p>Introduction, Basic Counting Principles, Mathematical Functions, Permutations
            Unit- 6 Combinations, the Pigeonhole Principle
        </p> 
            
            <h5>Introduction to Graphs</h5>
            <p>Terminology and special types of graphs, graph isomorphism
                Unit- 8 Paths, cycles and connectivity
                Unit- 9 Euler and Hamilton path and graphs </p>

                
            <h5>Graphs</h5>
            <p>
                shortest path problems, planner graphs,
               Unit- 11 graph coloring, chromatic number of graphs,
            </p>

            <h5>Introduction to Trees</h5>
            <p>tree and its properties, rooted tree
                Unit- 13 spanning and minimum spanning tree, binary search tree
                Unit- 14 infix, prefix, and post-fix notation, pre-order traversal, in-order traversal, and post-order
                traversal
                
            </p>

            <p>
                <h2>Course Outcomes:</h2>
                CO1: recall formal logical arguments of propositional logic <br />
                CO2: perceive problem solving through the basics of combinatorics <br />
                CO3: compare the basic discrete structures and algorithms <br />
                CO4: apply the concepts of trees to find the shortest path <br/>
                CO5: infer properties of graphs and be able to relate these to practical examples <br />
                CO6: formulate and prove theorems about trees, connectivity, coloring and planar graphs <br />
                
             </p>

