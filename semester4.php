        <p>
        <h2>Dynamic programming</h2>
        <h5>Introduction</h5>
        <p>.elementary data structures, basic computational models, analysis of
            algorithms: best case, average case and worst-case behaviour, asymptotic notations: big O
            notation, recursion, recurrence relations to analyse recursive algorithms
            </p>
        <h5>Dynamic Programming</h5>
        <p>general method, chained matrix multiplication, optimal storage
            on tapes
            Unit-5 More on Dynamic programming: all-pairs shortest paths, optimal binary search trees
            </p>
        <h5>BackTracking</h5>
        <p>comparison tree, oracles and adversary arguments
            Unit-11 More on lower bound theory: lower bounds through reductions
            
            </p> 
            
            <h5>Approximation:</h5>
            <p>approximation basics, task scheduling, bin packing</p>

                
            <h5>Lower bound theory:</h5>
            <p>Introduction, Address Space Allocation, Global Unicast Addresses,
                Autoconfiguration and Renumbering. IPv6 Protocol - Introduction , Packet Format, Transition
                from IPv4 to IPv6. ICMPv6 - Introduction, Error Messages, Informational Messages, NeighborDiscovery Messages, Group Membership Messages.
            </p>

            <h5>Intractable problems:</h5>
            <p>basic concepts, non-deterministic algorithms, NP completeness
                Unit-14 More on intractable problems: examples of NP-hard and NP-complete problems, cook's
                theorem, problem reduction
            </p>

            <p>
                <h2>Course Outcomes :</h2> 
        At the end of this course student will: <br>
        CO1: perceive the need of different algorithm design techniques <br />
        CO2: design and implement algorithms using divide and conquer, greedy approach, dynamic
        programming and backtracking <br />
        CO3: apply specific algorithms for solving computational problems like pattern matching, minimum
        spanning tree and shortest-path problems <br />
        CO4: analyze the asymptotic performance of algorithms <br />
             </p>

    </p>

   <br />
   <br/>
   <br />

    <p>
        <h2>Software Management</h2>
        <h5>Organizational Behaviors</h5>
        <p>relationship between management and organization behavior,
            model of OB and contributing disciplines to the OB field
            Foundations of individual behavior: values, attitude and job satisfaction, theories of
            learning and behavior modification</p>
        <h5>Personality:</h5>
        <p>theories of personality and its assessment, transactional analysis and
            attribution theory of perception
            Emotions: emotional intelligence and affective events theory of emotion
            Motivation: early and contemporary theories of motivation</p>
        <h5>Group dynamics: </h5>
        <p>conflict sources, types and levels of conflict,
            traditional and modern approaches to conflict, resolution of conflict through negotiation
            Stress: sources and consequences of stress, stress management techniques
            </p> 
            
            <h5>Job Analysis: </h5>
            <p>Methods of Collecting Job Data, Potential Problems with Job Analysis,
                Job Design and its approaches, Process of Job Analysis
                Unit-7 Recruitment & Selection: Meaning, Recruitment process, Recruitment Methods,
                Challenges in India and Selection Process</p>

                
            <h5>Talent Management: </h5>
            <p>
                talent management, talent retention, talent acquisition and
                sources of talent acquisition
                Orientation, induction and placement: process of orientation, induction and
                placement programme, Evaluation of Orientation Programme
                
            </p>

            <h5>Training and Development: </h5>
            <p>employee training, difference in training and
                development, methods of training, methods of management development, people
                capability maturity model
                Unit-10 Career planning and management: career management, process of career planning,
                challenges in career planning
                
            </p>

            <br />
   

            <p>
                <h2>Course Outcomes:</h2>
                CO1: enumerate the concept of management practices and organizational behavior <br />
                CO2: develop and sharpen acumen of how different management thoughts can be used to improve
                organization functioning<br/>
                CO3: analyze the importance of management practices and important organizational behavior
                dimensions at different levels of organization <br />
                CO4: appraise the dynamics of industrial relations and to manage them as per statutory regulations
                CO5: apply human resource management functions to handle emerging issues
                <br />
             </p>

