    <p>
        <h2>Linux Operating  System</h2>
        <h5>Getting started with Linux:</h5>
        <p>The History of UNIX and GNU–Linux, What Is So Good About
            Linux, Overview of Linux, Additional Features of Linux
            Unit- 2
            Installation Guide: Booting Linux Installation Program, Partitioning Hard Drives, Setting up
            Swap Space, Choosing Partitions to Format Booting with LILO, Multi-boot with Other Operating
            Systems, Logging In from a Terminal or Terminal Emulator, More About Logging In, Run levels</p>
        <h5>Connecting to Internet: </h5>
        <p>Network interfacing tool, Connecting to LAN, DNS (Static and
            Dynamic connection).
            Unit- 4
            Installing software: RPM management tool, Querying RPM packages, Package installation in
            TAR format, Adding & removing packages.</p>
        <h5>File Systems:</h5>
        <p>Obtaining User and System Information, Communicating with Other Users,
            Directory Files and Ordinary Files, Pathnames, Working with Directories, Access Permissions,
            Access Control Lists, Links.
            
            </p> 
            
            <h5>Working with Streams and Files:</h5>
            <p>C++ Streams, C++ Stream Classes, Classes for File Stream
                Operation, Opening & Closing Files, Detection of End of File
                : More about Open( ): File modes, File pointer & manipulator, Sequential Input &
                output Operation, Updating a File : Random Access, Command Line Arguments</p>

                
            <h5>The Bourne Again Shell and TC Shell:</h5>
            <p>
                : Shell Basics, Parameters and Variables, Special
                Characters, Processes, Re-executing and Editing Commands, Aliases, Functions, Controlling
                bash, Entering and Leaving the TC Shell, Features Common to the Bourne Again and TC Shells
                Unit- 9
                Programming the Bourne Again Shell: Control Structures, File Descriptors, Parameters and
                Variables, Built-in Commands, Expressions
            </p>

            <h5>Web Server Configuration:</h5>
            <p>Apache Web Server, Installing Apache, Configuring Web server,
                Starting Apache, Setting up first web page.
                Unit- 12 File Server Configuration: FTP protocol, Starting FTP server, Using FTP server, Using FTP
                client to test anonymous read access, Testing FTP server
                
            </p>

            <br />
   <br/>
   <br />

            <p>
                <h2>Course Outcomes:</h2>
                CO1: learn about Linux environment and basic Linux administration tasks.<br />
                CO2: demonstrate comprehensive introduction to shell scripting/programming in LINUX. <br />
                CO3: explain various basic Linux commands and C system programming and debugging techniques in Linux 
                environment. <br />
                CO4: analyze the usage of Linux utilities, organize directory structures, and develop useful shell scripts. <br />
                CO5: interpret and configure different Linux servers like samba, ftp, apache and nfs. <br />
             </p>

    </p>

