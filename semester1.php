    <p>
        <h2>Data Mining</h2>
        <h5>Data Warehousing and Online Analytical Processing:</h5>
        <p>.Modeling: Data Cube and OLAP, Data Warehouse Design and Usage, Data Warehouse
            Implementation
            Unit 2
            Introduction to data mining: Basic concepts of data mining, Different types of data
            repositories, Data mining functionalities, Concept of interesting patterns, Data mining tasks,
            Current trends, Major issues and ethics in data mining</p>
        <h5>Data Warehousing Architecture:</h5>
        <p>Operational Data and Data store, Load Manager, Warehouse
            Manager, Query Manager, Detailed Data, Lightly and highly summarized Data, Archive/Backup
            Data, Meta-Data, architecture model, 2-tier, 3-tier and 4-tier data warehouse, End user Access
            tools.
            Unit 4
            Installation and development environment overview: Downloading and installing Rapid
            miner and WEKA tool from source websites, Installing Rapid miner and WEKA tool on your
            windows computer</p>
        <h5>Introduction to mining tools: </h5>
        <p>Introduction to Rapid miner, Introduction to WEKA tool,
            features of tools, Comparison between Rapid Miner and WEKA, Overview of interface.
            Unit 6
            Extracting Data Sets: Importing data into Rapid miner using different formats of files, Storing
            and retrieving data using rapid miner, Graphical representation of data in rapid miner, Hands
            on practice problems on data import/export
            </p> 
            
            <h5>Data Preprocessing:</h5>
            <p>Identification and removal of duplicates, Apply
                operations for handling Meta data like rename or attribute role definition, Identify and remove
                the missing values in the data set, Apriori method for finding frequent item set WEKA / Rapid
                miner tool, Apply data mining pre-processing techniques and methods to large data sets,
                Hands on practice problems on data pre-processing
                </p>

                
            <h5>Clustering Algorithms and Cluster Analysis:</h5>
            <p>
            N: Measures of similarity, K means partitioning
            method, k medoids method, CLARANS method, Agglomerative and divisive clustering
            hierarchical method, BIRCH method,, Density based methods - Subspace clustering, Graphbased clustering - MST clustering, Cluster evaluation, Outlier detection and analysis
            
            </p>

            <h5>Applications of Data Warehousing and Data Mining:</h5>
            <p>Case studies of Data Warehousing in
                financial data analysis and retail industries, Case studies of Data Warehousing in Indian
                Railway reservation system and other industrial use, Case study on forecasting weather
                reports
                
            </p>

            <p>
                <h2>Course Outcomes:</h2>
                CO1: Understand the various concepts of data warehousing like metadata, data mart, summary table, fact
                data and dimension data. <br />
                CO2: Sail along with the various approaches in data mining. <br />
                CO3: Familiarize with the various data ware housing and data mining tools. <br />
                CO4: observe the various methods to extract knowledge using data mining techniques <br />
                CO5: evaluate current trends in data mining such as web mining, spatial-temporal mining. <br />
                CO6: apply different data mining methodologies with information systems. <br />
                CO7: research of database systems and able to improve the decision-making process. <br />
                
             </p>

    </p>

    <br />
    <br/>
