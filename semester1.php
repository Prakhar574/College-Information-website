    <p>
        <h2>C++ Programming</h2>
        <h5>Principles of OOPs and basics of C++:</h5>
        <p>.Basic Concepts of Object Oriented Programming, Object
            Oriented Languages, Benefits of OOP's Specifying Class, Access specifies, Defining member
            functions, Nesting of member functions, Private member functions, Arrays within class</p>
        <h5>Constructors and Destructors:</h5>
        <p>Constructors, Parameterized constructors, Copy Constructor
            and Dynamic Constructor, Multiple Constructor in a Class, Constructors with Default Arguments,
            Dynamic Initialization of Objects, Destructors</p>
        <h5>Inheritance:</h5>
        <p>Defining Derived Classes, Single Inheritance, Making a Private Member Inheritable,
            Multilevel Inheritance, Hierarchical Inheritance, Multiple Inheritance, Hybrid Inheritance
            </p> 
            
            <h5>Working with Streams and Files:</h5>
            <p>C++ Streams, C++ Stream Classes, Classes for File Stream
                Operation, Opening & Closing Files, Detection of End of File
                : More about Open( ): File modes, File pointer & manipulator, Sequential Input &
                output Operation, Updating a File : Random Access, Command Line Arguments</p>

                
            <h5>Generic Programming with Templates:</h5>
            <p>
            Need of Template, Class Template, Function Template,
            Overloading of Function Template Recursion with Template Function, Class Template and Inheritance,
            Difference between Templates and Macros
            </p>

            <h5>Exception Handling:</h5>
            <p>Principles of Exception Handling, Exception Handling Mechanism,
                Multiple Catch Statements, Catching Multiple Exceptions
                Unit 14 More on Exception Handling: Re-throwing Exceptions, Exceptions in Constructors and
                Destructors, Controlling Uncaught Exceptions
                
            </p>

            <br />
   

            <p>
                <h2>Course Outcomes:</h2>
      CO1: understand the concepts of Object-oriented programming <br/>
      CO2: distinguish between the procedure-oriented and object-oriented programming languages <br/>
      CO3: apply the concept of file handling and exception handling mechanisms<br/>  
      CO4: develop applications using the concepts of Object-oriented programming <br/>
      CO5: validate the code formulation by passing various test cases <br/>
             </p>

    </p>

