    <p>
        <h2>Software Development</h2>
        <h5>Introduction to software engineering:</h5>
        <p>: define software engineering, software process, software
            engineering practices
            Unit 2
            Software process models: software development life cycle (SDLC), classical software
            development lifecycle model, prototyping model, V model, incremental Model, introduction to
            agile method of software development</p>
        <h5>Requirement engineering:</h5>
        <p>requirement engineering, requirement eliciting/gathering,
            negotiating requirement, validating requirement, requirement analysis, stakeholder analysis
            Unit 4 Requirement specification: software requirement specification document, characteristics of a
            good SRS, functional and non-functional requirement</p>
        <h5>Design:</h5>
        <p>design process, design concepts, coupling, cohesion, data flow diagram (DFD), flow chart,
            architectural design, component-based design, object-oriented design, class-based components,
            use case diagram, class diagram, activity diagram
            Unit 6 User interface design: golden rules, interface design models, interface design process, interface
            design activities
            
            </p> 
            
            <h5>Software testing:</h5>
            <p>Ctest design, test planning, test case definition, test case template
                Unit 9 Testing strategies: black box testing, white box testing, sanity testing, smoke testing </p>

                
            <h5>Software maintenance</h5>
            <p>
                software maintenance, software supportability, reengineering, business
                process reengineering, software reengineering, restructuring, economics of reengineering
                Unit 13 Product metrics: measure, metrics and indicators, measurement principles, function-based
                metrics, metrics for specification quality
            </p>

            <h5>Software process improvement::</h5>
            <p>: approaches to SPI, maturity models, SPI process
                
            </p>

            <p>
                <h2>Course Outcomes:</h2>
                CO1: apply theoretical foundation of software engineering in practical software development <br />
                CO2: analyze the need of software maintenance activities <br />
                CO3: discuss the software life cycle models <br />
                CO4: apply software engineering practices to create complex software designs <br />
                CO5: identify the importance of the software development process <br />
             </p>

