        <p>
        <h2>Networking Fundamentals</h2>
        <h5>Network Devices</h5>
        <p>.The OSI Model and the TCP/IP Protocol Suite - Protocol Layers, The OSI Model, TCP/IP
            Protocol suite and Addressing.IPV4 Addresses- Introduction, Classful and Classless
            Addressing, Internet Protocol Version4(IPv4) – Datagrams, Fragmentation, Options,
            Checksum, Security, IP Package.</p>
        <h5>How OSI model works</h5>
        <p>.Know about osi model .Stages of osi model .Physical layer. .Presentation layer .Session layer . Netwrok layer .Transport layer .Appliaction layer TCP/IP Models</p>
        <h5>TCP/IP</h5>
        <p>Transmission Control Protocol – I : TCP Services, Features, Segment, TCP Connection,
            Windows in TCP.
            </p> 
            
            <h5>Transmission Control Protocol – II</h5>
            <p>Flow Control, Error Control, Congestion Control, TCP
                Timers, Options and TCP Package.</p>

                
            <h5>IP V6</h5>
            <p>Introduction, Address Space Allocation, Global Unicast Addresses,
                Autoconfiguration and Renumbering. IPv6 Protocol - Introduction , Packet Format, Transition
                from IPv4 to IPv6. ICMPv6 - Introduction, Error Messages, Informational Messages, NeighborDiscovery Messages, Group Membership Messages.
            </p>

            <h5>CyberSecurity</h5>
            <p>Introduction, Computer Security, Threats, Harm, Vulnerabilities,
                Controls, Authentication, Access Control and Cryptography.
                 Web attack: Browser Attacks, Web Attacks Targeting Users,
                Obtaining User or Website Data, Email Attacks.
                 Network Vulnerabilities: Overview of vulnerability scanning, Open
                Port / Service Identification, Banner /Version Check, Traffic Probe,
                Vulnerability Probe, Vulnerability Examples, OpenVAS,
                Metasploit.
            </p>

            <p>
                <h2>Course Outcomes :</h2> 
        At the end of this course student will: <br>
           CO1) Summarize basic principles of IPv4 and its Addressing mechanisms<br/>
           CO2) Understand UDP Services and Applications in Transport Layer<br/>
           CO3) Describe the services, and features of TCP <br/>
           CO4) Discuss various Flow , Error and Congestion control mechanisms of TCP <br/>
           CO5) Understand the Principles of IPv6 Addressing ,IPv6 and ICMPv6 Protocols
             </p>

    </p>

